const createRecipe = (config: {
  id: string;
  name: string;
  description: string;
  ingredients: Array<{ foodId: string; quantity: number; unit: string; notes?: string }>;
  instructions: string[];
  prepTime: number;
  cookTime: number;
  difficulty: 'easy' | 'medium' | 'hard';
  calories: number;
  mealType: 'breakfast' | 'lunch' | 'dinner' | 'snack';
  cuisineType: Recipe['cuisineType'];
  cookingMethod: Recipe['cookingMethod'];
  tags: string[];
  imageKey: keyof typeof RECIPE_IMAGES.breakfast;
  hasVegetables: boolean;
}): Recipe => {
  const nutrition = createNutritionInfo(config.calories);
  const healthScore = calculateHealthScore(
    config.calories,
    nutrition.protein,
    config.hasVegetables,
    'minimally-processed'
  );

  const ingredientNames = config.ingredients.map(i => i.foodId.toLowerCase());
  const glutenFree = !ingredientNames.some(n => n.includes('wheat') || n.includes('flour') || n.includes('bread'));
  const dairyFree = !ingredientNames.some(n => n.includes('milk') || n.includes('cheese') || n.includes('butter'));
  const hasMeat = ingredientNames.some(n => n.includes('chicken') || n.includes('beef') || n.includes('meat'));
  const hasFish = ingredientNames.some(n => n.includes('fish') || n.includes('salmon') || n.includes('shrimp'));

  const dietaryRestrictions: DietaryRestriction[] = [];
  if (!hasMeat && !hasFish) dietaryRestrictions.push('vegetarian');
  if (!hasMeat && !hasFish && !dairyFree) dietaryRestrictions.push('gluten-free');
  if (glutenFree && !dietaryRestrictions.includes('gluten-free')) dietaryRestrictions.push('gluten-free');
  if (dairyFree) dietaryRestrictions.push('dairy-free');

  const allergens: AllergenType[] = [];
  if (!glutenFree) allergens.push('gluten');
  if (!dairyFree) allergens.push('dairy');
  if (ingredientNames.some(n => n.includes('egg'))) allergens.push('eggs');

  return {
    id: config.id,
    name: config.name,
    description: config.description,
    ingredients: config.ingredients,
    instructions: config.instructions,
    servings: 2,
    prepTime: config.prepTime,
    cookTime: config.cookTime,
    totalTime: config.prepTime + config.cookTime,
    difficulty: config.difficulty,
    nutritionPerServing: nutrition,
    photos: [RECIPE_IMAGES[config.mealType][config.imageKey] as string],
    tags: config.tags,
    verified: true,
    createdAt: '2024-01-01T00:00:00.000Z',
    updatedAt: '2024-01-01T00:00:00.000Z',
    healthScore,
    allergens: allergens.length > 0 ? allergens : undefined,
    processingLevel: 'minimally-processed',
    dietaryRestrictions: dietaryRestrictions.length > 0 ? dietaryRestrictions : undefined,
    mealType: config.mealType,
    cuisineType: config.cuisineType,
    cookingMethod: config.cookingMethod,
    rating: 4 + Math.random(),
    ratingCount: Math.floor(50 + Math.random() * 200),
    saveCount: Math.floor(100 + Math.random() * 500),
    viewCount: Math.floor(500 + Math.random() * 2000),
    author: {
      id: 'chef_caloritrack',
      name: 'CaloriTrack Kitchen',
      isVerified: true,
    },
  };
};

